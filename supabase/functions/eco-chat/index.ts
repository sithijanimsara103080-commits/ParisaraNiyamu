import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are EcoBot 🌿, the friendly AI assistant for Radapasa Environmental Pioneers Society at Rajapaksa Central College, Weeraketiya. You are knowledgeable about:
- Environmental science, ecology, climate change, sustainability
- The school's Environment Unit activities: tree planting, recycling drives, awareness campaigns
- Eco-friendly tips and practices for students
- How to join the unit and upcoming events

You support trilingual communication:
- English (default)
- Sinhala (සිංහල)
- Tamil (தமிழ்)

Respond in the same language the user writes in. Keep answers concise, friendly, and educational. Use emojis occasionally. If asked about something unrelated to the environment or school, gently redirect to eco topics.

Key info:
- School: Rajapaksa Central College, Weeraketiya
- Unit meets every Friday at the Science Block during lunch
- President: Kavinda Silva, Secretary: Amaya Fernando
- Upcoming: World Ocean Day Beach Cleanup (Jun 8), Inter-house Eco Quiz (Jun 20), Annual Tree Planting (Jul 5)
- The unit has planted 1250+ trees and collected 840kg of plastic`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const response = await fetch(
      "https://ai.gateway.lovable.dev/v1/chat/completions",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${LOVABLE_API_KEY}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model: "google/gemini-3-flash-preview",
          messages: [
            { role: "system", content: SYSTEM_PROMPT },
            ...messages,
          ],
          stream: true,
        }),
      }
    );

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limited. Please try again shortly." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI credits exhausted. Please add funds." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(
        JSON.stringify({ error: "AI service error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("eco-chat error:", e);
    return new Response(
      JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
